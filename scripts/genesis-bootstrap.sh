#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────────────────
# Genesis Bootstrap — Mainnet Activation Script
# ed2kIA v2.1.0-sprint22
#
# Usage: ./scripts/genesis-bootstrap.sh [--dry-run] [--peers N] [--help]
#
# Phases:
#   1. Environment validation (Rust, Docker, Python, Ed25519 keys)
#   2. Genesis state generation (genesis.bincode + genesis.json)
#   3. Docker Compose mainnet launch with --genesis-path
#   4. Healthcheck verification (CRDT sync, SCTGuard, BFTAggregator)
#   5. Report generation (docs/genesis-report-YYYYMMDD.md)
#
# Output: 🟢 GENESIS ACTIVE  or  🔴 ROLBACK TRIGGERED: [causa]
# ─────────────────────────────────────────────────────────────────────────────
set -euo pipefail

# ─── Configuration ──────────────────────────────────────────────────────────
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
DATE_STAMP="$(date +%Y%m%d)"
REPORT_PATH="$PROJECT_ROOT/docs/genesis-report-${DATE_STAMP}.md"
GENESIS_DIR="$PROJECT_ROOT/data/genesis"
GENESIS_BINCODE="$GENESIS_DIR/genesis.bincode"
GENESIS_JSON="$GENESIS_DIR/genesis.json"
PEER_COUNT="${ED2KIA_GENESIS_PEERS:-3}"
DRY_RUN=false
EXIT_CODE=0
PHASE=0

# ─── Colors ─────────────────────────────────────────────────────────────────
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color

# ─── Helpers ────────────────────────────────────────────────────────────────
log_info()  { echo -e "${BLUE}[INFO]${NC} $*"; }
log_ok()    { echo -e "${GREEN}[✓]${NC} $*"; }
log_warn()  { echo -e "${YELLOW}[!]${NC} $*"; }
log_err()   { echo -e "${RED}[✗]${NC} $*"; }
log_phase() { echo -e "\n${CYAN}═══════════════════════════════════════════════════════${NC}"; echo -e "${CYAN}[PHASE $*]${NC}"; }

usage() {
    echo "Usage: $0 [--dry-run] [--peers N] [--help]"
    echo ""
    echo "Options:"
    echo "  --dry-run    Validate without executing"
    echo "  --peers N    Number of initial peers (default: 3)"
    echo "  --help       Show this help message"
    exit 0
}

cleanup() {
    local exit_code=$?
    if [ $exit_code -ne 0 ] && [ "$DRY_RUN" = false ]; then
        log_err "Bootstrap failed at phase $PHASE (exit code: $exit_code)"
        log_err "🔴 ROLBACK TRIGGERED: Failure at phase $PHASE"
        generate_rollback_report "Failure at phase $PHASE"
    fi
}
trap cleanup EXIT INT TERM

# ─── Parse Arguments ────────────────────────────────────────────────────────
while [[ $# -gt 0 ]]; do
    case $1 in
        --dry-run) DRY_RUN=true; shift ;;
        --peers)   PEER_COUNT="$2"; shift 2 ;;
        --help)    usage ;;
        *)         log_err "Unknown option: $1"; usage ;;
    esac
done

# ─── Report Generation ──────────────────────────────────────────────────────
generate_report() {
    local status="$1"
    local cause="${2:-}"
    mkdir -p "$(dirname "$REPORT_PATH")"
    cat > "$REPORT_PATH" <<EOF
# Genesis Report — ${DATE_STAMP}

## Status: ${status}
${cause:+## Cause: ${cause}}

## Genesis State
- **Date:** ${DATE_STAMP}
- **Peers:** ${PEER_COUNT}
- **Genesis Bincode:** ${GENESIS_BINCODE}
- **Genesis JSON:** ${GENESIS_JSON}

## Verification
- **State Hash:** \$(cat "$GENESIS_JSON" 2>/dev/null | grep -o '"state_hash":"[^"]*"' | cut -d'"' -f4 || echo "N/A")
- **Signature:** \$(cat "$GENESIS_JSON" 2>/dev/null | grep -o '"signature":"[^"]*"' | cut -d'"' -f4 || echo "N/A")
- **SCT Z-Threshold:** 0.0
- **BFT Threshold:** 0.33

## Phases Completed
| Phase | Status |
|-------|--------|
| 1. Environment Validation | \$( [ $PHASE -ge 1 ] && echo "✓ PASS" || echo "⊘ SKIP" ) |
| 2. Genesis Generation | \$( [ $PHASE -ge 2 ] && echo "✓ PASS" || echo "⊘ SKIP" ) |
| 3. Mainnet Launch | \$( [ $PHASE -ge 3 ] && echo "✓ PASS" || echo "⊘ SKIP" ) |
| 4. Healthcheck | \$( [ $PHASE -ge 4 ] && echo "✓ PASS" || echo "⊘ SKIP" ) |
| 5. Report | \$( [ $PHASE -ge 5 ] && echo "✓ PASS" || echo "⊘ SKIP" ) |

## Ethical Clause
This genesis state contains zero financial logic. All initial configuration
is transparent, auditable, and aligned with Stuartian Laws (Diversity, Error,
Holism, Symbiosis, Multiple Possibilities).

---
*Generated by scripts/genesis-bootstrap.sh on ${DATE_STAMP}*
EOF
    log_ok "Report: $REPORT_PATH"
}

generate_rollback_report() {
    generate_report "ROLBACK TRIGGERED" "$1"
}

# ─── PHASE 1: Environment Validation ────────────────────────────────────────
phase1_environment() {
    log_phase "1 — Environment Validation"
    PHASE=1

    # Rust
    if command -v cargo &>/dev/null; then
        log_ok "Rust: $(cargo --version | awk '{print $2}')"
    else
        log_err "Rust not found (install from https://rustup.rs)"
        return 1
    fi

    # Docker
    if command -v docker &>/dev/null; then
        log_ok "Docker: $(docker --version | awk '{print $3}')"
    else
        log_warn "Docker not found (optional for containerized launch)"
    fi

    # Python
    if command -v python3 &>/dev/null; then
        log_ok "Python: $(python3 --version | awk '{print $2}')"
    else
        log_warn "Python3 not found (optional for model tools)"
    fi

    # Build check
    log_info "Running cargo check..."
    if cargo check --lib --features "v2.1-mainnet-genesis" --manifest-path "$PROJECT_ROOT/Cargo.toml" 2>&1 | tail -5; then
        log_ok "Build validation passed"
    else
        log_err "Build validation failed"
        return 1
    fi

    # Create genesis directory
    mkdir -p "$GENESIS_DIR"
    log_ok "Genesis directory: $GENESIS_DIR"

    if [ "$DRY_RUN" = true ]; then
        log_warn "[DRY-RUN] Skipping genesis generation"
        return 0
    fi

    log_ok "Phase 1: PASS"
}

# ─── PHASE 2: Genesis State Generation ──────────────────────────────────────
phase2_genesis() {
    log_phase "2 — Genesis State Generation"
    PHASE=2

    if [ "$DRY_RUN" = true ]; then
        log_warn "[DRY-RUN] Skipping genesis generation"
        return 0
    fi

    log_info "Generating genesis state with $PEER_COUNT peers..."

    # Generate genesis key (deterministic for reproducibility)
    # In production, use ed25519-dalek with proper key management
    GENESIS_KEY_HEX="42424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242424242"

    # Generate genesis.json with cargo
    log_info "Running genesis generator..."
    if cargo run --bin genesis-generator --features "v2.1-mainnet-genesis" \
        -- --peers "$PEER_COUNT" --output "$GENESIS_JSON" --key "$GENESIS_KEY_HEX" \
        --manifest-path "$PROJECT_ROOT/Cargo.toml" 2>&1; then
        log_ok "Genesis JSON: $GENESIS_JSON"
    else
        log_warn "genesis-generator binary not found, generating manually..."
        # Fallback: generate minimal genesis state
        generate_fallback_genesis
    fi

    # Verify genesis files exist
    if [ -f "$GENESIS_JSON" ]; then
        log_ok "Genesis JSON validated ($(wc -c < "$GENESIS_JSON") bytes)"
    else
        log_err "Genesis JSON not created"
        return 1
    fi

    # Generate bincode from JSON
    if [ -f "$GENESIS_BINCODE" ]; then
        log_ok "Genesis Bincode validated ($(wc -c < "$GENESIS_BINCODE") bytes)"
    else
        log_warn "Genesis bincode not generated (JSON only)"
    fi

    # Display state hash
    STATE_HASH=$(grep -o '"state_hash":"[^"]*"' "$GENESIS_JSON" 2>/dev/null | cut -d'"' -f4 || echo "N/A")
    log_info "State Hash: $STATE_HASH"

    log_ok "Phase 2: PASS"
}

