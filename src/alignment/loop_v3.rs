//! Alignment Loop v3 — Loop de alineación con verificación ZKP y mitigación de sesgos
//!
//! v3 introduces ZKP-verified steering signals, bias detection,
//! and confidence-based fallback to static mode when bias_score > 0.15.
//!
//! Feature-gated: `#[cfg(feature = "v1.2-sprint4")]`

use serde::{Deserialize, Serialize};
use sha2::{Digest, Sha256};
use std::collections::{BTreeMap, VecDeque};
use thiserror::Error;
use tracing::{debug, info, warn};

// ---------------------------------------------------------------------------
// Errors
// ---------------------------------------------------------------------------

/// Errors for Alignment Loop v3.
#[derive(Debug, Error)]
pub enum AlignmentV3Error {
    #[error("Invalid feedback: {0}")]
    InvalidFeedback(String),
    #[error("Steering verification failed")]
    SteeringVerificationFailed,
    #[error("Bias threshold exceeded: {score:.3} > {threshold:.3}")]
    BiasThresholdExceeded { score: f64, threshold: f64 },
    #[error("Confidence below minimum: {score:.3} < {min:.3}")]
    ConfidenceBelowMinimum { score: f64, min: f64 },
    #[error("No feedback data available")]
    NoFeedbackData,
    #[error("Model layer not found: {0}")]
    LayerNotFound(String),
    #[error("ZKP proof generation failed")]
    ZKPProofFailed,
}

