/// Alignment loop v3 with ZKP verification and bias mitigation.
pub struct AlignmentLoopV3 {
    config: AlignmentV3Config,
    feedback_buffer: VecDeque<FeedbackEntryV3>,
    signals: Vec<SteeringSignalV3>,
    stats: AlignmentStatsV3,
    current_cycle: u64,
    /// Per-layer feedback aggregation
    layer_scores: BTreeMap<String, VecDeque<f64>>,
}

impl AlignmentLoopV3 {
    /// Creates a new alignment loop with default config.
    pub fn new() -> Self {
        Self::with_config(AlignmentV3Config::default())
    }

    /// Creates a loop with custom config.
    pub fn with_config(config: AlignmentV3Config) -> Self {
        Self {
            config,
            feedback_buffer: VecDeque::new(),
            signals: Vec::new(),
            stats: AlignmentStatsV3::default(),
            current_cycle: 0,
            layer_scores: BTreeMap::new(),
        }
    }

    /// Submits feedback to the alignment loop.
    pub fn submit_feedback(&mut self, feedback: FeedbackEntryV3) -> Result<(), AlignmentV3Error> {
        // Verify ZKP proof
        if !feedback.verify_proof() {
            return Err(AlignmentV3Error::SteeringVerificationFailed);
        }

        // Check confidence
        if feedback.confidence < self.config.min_confidence {
            return Err(AlignmentV3Error::ConfidenceBelowMinimum {
                score: feedback.confidence,
                min: self.config.min_confidence,
            });
        }

        self.feedback_buffer.push_back(feedback.clone());
        self.stats.total_feedback += 1;

        // Track per-layer scores
        self.layer_scores
            .entry(feedback.layer_id)
            .or_default()
            .push_back(feedback.score);

        // Enforce window size
        while self.feedback_buffer.len() > self.config.feedback_window {
            self.feedback_buffer.pop_front();
        }
        for scores in self.layer_scores.values_mut() {
            while scores.len() > self.config.feedback_window {
                scores.pop_front();
            }
        }

        info!(
            "AlignmentV3: submitted feedback from {} (score={:.3})",
            feedback.source_id, feedback.score
        );
        Ok(())
    }

    /// Runs an alignment cycle.
    pub fn run_cycle(&mut self) -> Result<AlignmentResultV3, AlignmentV3Error> {
        if self.feedback_buffer.is_empty() {
            return Err(AlignmentV3Error::NoFeedbackData);
        }

        self.current_cycle += 1;
        let mut cycle_signals = Vec::new();
        let mut total_alignment = 0.0;
        let mut total_confidence = 0.0;
        let mut layer_count = 0;
        let mut max_bias: f64 = 0.0;

        // Process each monitored layer
        for layer_id in &self.config.monitored_layers {
            let scores = self.layer_scores.get(layer_id);
            match scores {
                Some(scores) if !scores.is_empty() => {
                    let avg_score: f64 = scores.iter().sum::<f64>() / scores.len() as f64;
                    let confidence = self.compute_confidence(scores);
                    let bias_score = self.compute_bias_score(scores);

                    // Check bias threshold
                    if bias_score > self.config.max_bias_score {
                        self.stats.bias_detections += 1;
                        warn!(
                            "AlignmentV3: bias detected in {} (score={:.3})",
                            layer_id, bias_score
                        );
                    }
                    max_bias = max_bias.max(bias_score);

                    // Generate steering signal
                    let adjustment = self.config.adjustment_rate * avg_score;
                    let signal = SteeringSignalV3::new(
                        format!("sig-{}-{}", self.current_cycle, layer_id),
                        layer_id.clone(),
                        adjustment,
                        confidence,
                        bias_score,
                    );

                    cycle_signals.push(signal);
                    total_alignment += avg_score.abs();
                    total_confidence += confidence;
                    layer_count += 1;
                }
                _ => {
                    debug!("AlignmentV3: no feedback for layer {}", layer_id);
                }
            }
        }

        let avg_alignment = if layer_count > 0 {
            total_alignment / layer_count as f64
        } else {
            0.0
        };
        let avg_confidence = if layer_count > 0 {
            total_confidence / layer_count as f64
        } else {
            0.0
        };

        let fallback =
            max_bias > self.config.max_bias_score || avg_confidence < self.config.min_confidence;
        if fallback {
            self.stats.fallback_count += 1;
        }

        let result = AlignmentResultV3 {
            cycle: self.current_cycle,
            signals: cycle_signals,
            alignment_score: avg_alignment,
            confidence: avg_confidence,
            bias_score: max_bias,
            fallback_to_static: fallback,
            timestamp_ms: current_timestamp_ms(),
        };

        // Update stats
        self.stats.total_cycles += 1;
        self.stats.total_signals += result.signals.len();
        let alpha = 0.1;
        self.stats.avg_alignment_score =
            alpha * result.alignment_score + (1.0 - alpha) * self.stats.avg_alignment_score;
        self.stats.avg_confidence =
            alpha * result.confidence + (1.0 - alpha) * self.stats.avg_confidence;

        self.signals.extend(result.signals.clone());

        info!(
            "AlignmentV3: cycle {} complete (alignment={:.3}, bias={:.3}, fallback={})",
            result.cycle, result.alignment_score, result.bias_score, result.fallback_to_static
        );

        Ok(result)
    }

    /// Gets alignment statistics.
    pub fn get_stats(&self) -> AlignmentStatsV3 {
        self.stats.clone()
    }

    /// Gets recent signals.
    pub fn get_recent_signals(&self, limit: usize) -> Vec<&SteeringSignalV3> {
        self.signals.iter().rev().take(limit).collect()
    }

    /// Gets pending feedback count.
    pub fn pending_feedback_count(&self) -> usize {
        self.feedback_buffer.len()
    }

    /// Clears all feedback.
    pub fn clear_feedback(&mut self) {
        self.feedback_buffer.clear();
        self.layer_scores.clear();
    }

    /// Computes confidence from score distribution.
    fn compute_confidence(&self, scores: &VecDeque<f64>) -> f64 {
        if scores.len() < 2 {
            return 0.5;
        }
        let mean: f64 = scores.iter().sum::<f64>() / scores.len() as f64;
        let variance: f64 =
            scores.iter().map(|s| (s - mean).powi(2)).sum::<f64>() / scores.len() as f64;
        let std_dev = variance.sqrt();

        // Lower variance = higher confidence
        (1.0 / (1.0 + std_dev)).min(1.0)
    }

    /// Computes bias score from score distribution.
    fn compute_bias_score(&self, scores: &VecDeque<f64>) -> f64 {
        if scores.is_empty() {
            return 0.0;
        }
        let mean: f64 = scores.iter().sum::<f64>() / scores.len() as f64;

        // Check for skew (bias indicator)
        let skew: f64 = if scores.len() >= 3 {
            let variance: f64 =
                scores.iter().map(|s| (s - mean).powi(2)).sum::<f64>() / scores.len() as f64;
            let std_dev = variance.sqrt().max(1e-10);
            let cubed: f64 =
                scores.iter().map(|s| (s - mean).powi(3)).sum::<f64>() / scores.len() as f64;
            (cubed / std_dev.powi(3)).abs()
        } else {
            0.0
        };

        // Check for concentration (all scores same direction)
        let positive = scores.iter().filter(|&&s| s > 0.0).count() as f64;
        let concentration = (positive / scores.len() as f64 - 0.5).abs() * 2.0;

        (skew * 0.6 + concentration * 0.4).min(1.0)
    }
}

impl Default for AlignmentLoopV3 {
    fn default() -> Self {
        Self::new()
    }
}

