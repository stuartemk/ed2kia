/// Type of alignment feedback.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum FeedbackTypeV3 {
    /// Human preference feedback.
    HumanPreference,
    /// Automated metric feedback.
    AutomatedMetric,
    /// Cross-model consensus feedback.
    CrossModelConsensus,
    /// Ethical constraint feedback.
    EthicalConstraint,
}

impl std::fmt::Display for FeedbackTypeV3 {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            FeedbackTypeV3::HumanPreference => write!(f, "HUMAN_PREF"),
            FeedbackTypeV3::AutomatedMetric => write!(f, "AUTO_METRIC"),
            FeedbackTypeV3::CrossModelConsensus => write!(f, "CROSS_MODEL"),
            FeedbackTypeV3::EthicalConstraint => write!(f, "ETHICAL"),
        }
    }
}

/// Alignment feedback entry.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FeedbackEntryV3 {
    /// Source identifier.
    pub source_id: String,
    /// Feedback type.
    pub feedback_type: FeedbackTypeV3,
    /// Score (-1.0 to 1.0).
    pub score: f64,
    /// Confidence (0.0 to 1.0).
    pub confidence: f64,
    /// Model layer affected.
    pub layer_id: String,
    /// Timestamp (ms).
    pub timestamp_ms: u64,
    /// ZKP proof hash for verification.
    pub proof_hash: String,
}

impl FeedbackEntryV3 {
    /// Creates a new feedback entry with ZKP proof.
    pub fn new(
        source_id: String,
        feedback_type: FeedbackTypeV3,
        score: f64,
        confidence: f64,
        layer_id: String,
    ) -> Self {
        let proof_hash = compute_feedback_proof(&source_id, score, confidence);
        Self {
            source_id,
            feedback_type,
            score: score.clamp(-1.0, 1.0),
            confidence: confidence.clamp(0.0, 1.0),
            layer_id,
            timestamp_ms: current_timestamp_ms(),
            proof_hash,
        }
    }

    /// Verifies the ZKP proof hash.
    pub fn verify_proof(&self) -> bool {
        let expected = compute_feedback_proof(&self.source_id, self.score, self.confidence);
        self.proof_hash == expected
    }
}

/// Steering signal generated by the alignment loop.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SteeringSignalV3 {
    /// Signal identifier.
    pub signal_id: String,
    /// Target layer.
    pub layer_id: String,
    /// Adjustment vector (magnitude).
    pub adjustment: f64,
    /// Confidence score.
    pub confidence: f64,
    /// ZKP proof for verification.
    pub zkp_proof: String,
    /// Bias score (0.0 = no bias, 1.0 = high bias).
    pub bias_score: f64,
    /// Timestamp (ms).
    pub timestamp_ms: u64,
}

impl SteeringSignalV3 {
    /// Creates a new steering signal.
    pub fn new(
        signal_id: String,
        layer_id: String,
        adjustment: f64,
        confidence: f64,
        bias_score: f64,
    ) -> Self {
        let zkp_proof = compute_steering_proof(&signal_id, &layer_id, adjustment);
        Self {
            signal_id,
            layer_id,
            adjustment: adjustment.clamp(-1.0, 1.0),
            confidence: confidence.clamp(0.0, 1.0),
            zkp_proof,
            bias_score: bias_score.clamp(0.0, 1.0),
            timestamp_ms: current_timestamp_ms(),
        }
    }

    /// Verifies the ZKP proof.
    pub fn verify_zkp(&self) -> bool {
        let expected = compute_steering_proof(&self.signal_id, &self.layer_id, self.adjustment);
        self.zkp_proof == expected
    }

    /// Returns true if bias is acceptable.
    pub fn is_bias_acceptable(&self, threshold: f64) -> bool {
        self.bias_score <= threshold
    }
}

/// Result of an alignment cycle.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AlignmentResultV3 {
    /// Cycle number.
    pub cycle: u64,
    /// Generated steering signals.
    pub signals: Vec<SteeringSignalV3>,
    /// Overall alignment score.
    pub alignment_score: f64,
    /// Overall confidence.
    pub confidence: f64,
    /// Bias score.
    pub bias_score: f64,
    /// Fallback to static mode.
    pub fallback_to_static: bool,
    /// Timestamp (ms).
    pub timestamp_ms: u64,
}

/// Statistics for the alignment loop.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AlignmentStatsV3 {
    /// Total cycles completed.
    pub total_cycles: u64,
    /// Total feedback processed.
    pub total_feedback: usize,
    /// Total signals generated.
    pub total_signals: usize,
    /// Average alignment score.
    pub avg_alignment_score: f64,
    /// Average confidence.
    pub avg_confidence: f64,
    /// Fallback count.
    pub fallback_count: usize,
    /// Bias detections.
    pub bias_detections: usize,
}

impl Default for AlignmentStatsV3 {
    fn default() -> Self {
        Self {
            total_cycles: 0,
            total_feedback: 0,
            total_signals: 0,
            avg_alignment_score: 0.5,
            avg_confidence: 0.5,
            fallback_count: 0,
            bias_detections: 0,
        }
    }
}

/// Configuration for Alignment Loop v3.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AlignmentV3Config {
    /// Minimum confidence threshold.
    pub min_confidence: f64,
    /// Maximum acceptable bias score.
    pub max_bias_score: f64,
    /// Feedback window size.
    pub feedback_window: usize,
    /// Steering adjustment rate.
    pub adjustment_rate: f64,
    /// Number of layers to monitor.
    pub monitored_layers: Vec<String>,
}

impl Default for AlignmentV3Config {
    fn default() -> Self {
        Self {
            min_confidence: 0.88,
            max_bias_score: 0.15,
            feedback_window: 100,
            adjustment_rate: 0.1,
            monitored_layers: vec![
                "layer_0".to_string(),
                "layer_1".to_string(),
                "layer_2".to_string(),
            ],
        }
    }
}

