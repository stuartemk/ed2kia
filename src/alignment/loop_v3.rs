/// Computes ZKP proof hash for feedback.
pub fn compute_feedback_proof(source_id: &str, score: f64, confidence: f64) -> String {
    let mut hasher = Sha256::new();
    hasher.update(b"zkp-feedback-v3:");
    hasher.update(source_id.as_bytes());
    hasher.update(score.to_le_bytes());
    hasher.update(confidence.to_le_bytes());
    let result = hasher.finalize();
    format!("0x{}", hex::encode(result))
}

/// Computes ZKP proof hash for steering signal.
pub fn compute_steering_proof(signal_id: &str, layer_id: &str, adjustment: f64) -> String {
    let mut hasher = Sha256::new();
    hasher.update(b"zkp-steering-v3:");
    hasher.update(signal_id.as_bytes());
    hasher.update(layer_id.as_bytes());
    hasher.update(adjustment.to_le_bytes());
    let result = hasher.finalize();
    format!("0x{}", hex::encode(result))
}

/// Returns current timestamp in milliseconds.
fn current_timestamp_ms() -> u64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis() as u64
}

